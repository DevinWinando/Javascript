// Operasi Penjumlahan //
console.log(10 + 5);
// 15

/**
 *    contoh perbedaan operator penjumlahan dan concatenation. semua Integer
 *   akan berubah menjadi string apabila bertemu dengan
 *  concatenation dan akan menghasilkan string juga.
 */
console.log(3 + 3); // 6
console.log(3 + "3"); // "33"
console.log(3 + 3 + "3"); // "63"
console.log(3 + "3" + 3); // "333"

// Operasi Pengurangan //
console.log(7 - 2);
// output 5

// Perkalian //
console.log(10 * 5);
// output 50

// Pembagian //
console.log(8 / 4);
// output 2

//=== Modulus ===//
console.log(7 % 2);
// output 1

//=== Eksponen ===//
console.log(3 ** 3);
// output 3 * 3 * 3 = 27

// increment //
let x = 10;
console.log(x++, x++, x++, x++);
// output 10, 11, 12, 13

// Decrement
let y = 5;
console.log(y--, y--, y--, y--);
// 5, 4, 3, 2
